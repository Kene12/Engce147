# สร้างเซ็ตของเลขคู่ตั้งแต่ 1 ถึง 10
even_numbers = {x for x in range(1, 11) if x % 2 == 0}  # ใช้ set comprehension เพื่อสร้างเซ็ตที่ประกอบด้วยเลขคู่
# range(1, 11) จะสร้างช่วงตัวเลขตั้งแต่ 1 ถึง 10
# if x % 2 == 0 จะคัดเลือกเฉพาะตัวเลขที่เป็นเลขคู่ (หารด้วย 2 ลงตัว)

# แสดงผลเซ็ตของเลขคู่
print("Even numbers:", even_numbers)  # Output: Even numbers: {2, 4, 6, 8, 10} แสดงผลเซ็ตของเลขคู่ที่สร้างขึ้น