# สร้างรายการตัวเลข
numbers = [1, 2, 3, 4, 5]  # เริ่มต้นรายการด้วยตัวเลข 1, 2, 3, 4, 5

# เพิ่มตัวเลขไปที่ท้ายรายการ
numbers.append(6)  # เพิ่มเลข 6 เข้าไปที่ท้ายสุดของรายการ

# แก้ไของค์ประกอบที่ตำแหน่งที่กำหนด
numbers[2] = 10  # เปลี่ยนค่าขององค์ประกอบที่ตำแหน่งที่ 2 (ตำแหน่งเริ่มจาก 0) เป็น 10

# ลบตัวเลขที่กำหนด
numbers.remove(4)  # ลบองค์ประกอบที่มีค่าเป็น 4 ออกจากรายการ

# แสดงรายการที่อัพเดทแล้ว
print("Updated list:", numbers)  # แสดงผลรายการที่ได้รับการแก้ไขแล้ว ซึ่งควรจะเป็น [1, 2, 10, 5, 6]