# กำหนดรายการตัวเลข
numbers = [5, 2, 8, 1, 3]  # สร้างรายการตัวเลขเริ่มต้นด้วยค่า 5, 2, 8, 1, 3

# เรียงลำดับรายการ
numbers.sort()  # ใช้วิธี .sort() เพื่อเรียงลำดับค่าของตัวเลขในรายการจากน้อยไปมาก
print("Sorted list:", numbers)  # Output: [1, 2, 3, 5, 8] แสดงผลรายการที่ถูกเรียงลำดับแล้ว

# ย้อนกลับรายการ
numbers.reverse()  # ใช้วิธี .reverse() เพื่อย้อนกลับค่าของตัวเลขในรายการ
print("Reversed list:", numbers)  # Output: [8, 5, 3, 2, 1] แสดงผลรายการที่ถูกย้อนกลับแล้ว