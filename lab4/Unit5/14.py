# สร้างรายการของจำนวนกำลังสองของตัวเลขจาก 1 ถึง 5
squares = [x ** 2 for x in range(1, 6)]  # ใช้ list comprehension เพื่อสร้างรายการของจำนวนกำลังสองของตัวเลขจาก 1 ถึง 5
# range(1, 6) จะสร้างช่วงตัวเลขตั้งแต่ 1 ถึง 5
# x ** 2 จะเป็นการยกกำลังสองของตัวเลข x และสร้างรายการที่มีค่าของ x ** 2 สำหรับแต่ละ x ในช่วงตัวเลข

# แสดงรายการของจำนวนกำลังสอง
print("Squares:", squares)  # Output: [1, 4, 9, 16, 25]แสดงผลรายการของจำนวนกำลังสองที่สร้างขึ้น